    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        String fullName = fullNameField.getText().trim();
        String studentId = studentIdField.getText().trim();
        String email = emailField.getText().trim();
        String type = (String) TypeComboBox.getSelectedItem();
        
        if (fullName.isEmpty()) {
            showError("Please enter student name.");
            fullNameField.requestFocus();
            return;
        }
        
        if (studentId.isEmpty()) {
            showError("Please enter student id.");
            studentIdField.requestFocus();
            return;
        }
        
        if (studentId.length() != 6) {
            showError("Please enter 6 didgit student id.");
            studentIdField.requestFocus();
            return;
        }
        
        if (email.isEmpty()) {
            showError("Please enter student email");
            emailField.requestFocus();
            return;
        }
        
        if (!email.contains("@")|| !email.contains(".")) {
            showError("Please input a valid email.");
            emailField.requestFocus();
            return;
        } 
        
        //All validation passes
        
        
        // Display message to user from message label in GUI
        MessageLabel.setText("Successful Registration!");
        MessageLabel.setForeground(Color.green); 
        
        //Display Registration info to user through JOptionPane
        String message = "Registration Details: \n" +
                "Name: " + fullName + "\n" +
                "Student Id: " + studentId + "\n" +
                "Email: " + email + "\n" +
                "Full Time/Part Time: " + type + "\n" +
                "Registration completed successfully";
        
        JOptionPane.showMessageDialog(this, message);
        
        //Write the reg detail to a textfile
        WriteToFile(fullName, studentId, email, type);
        
        
        
    }//GEN-LAST:event_SubmitButtonActionPerformed

