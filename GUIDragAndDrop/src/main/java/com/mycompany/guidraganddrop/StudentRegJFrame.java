    private void showError ( String Message){
        MessageLabel.setText(Message);
        MessageLabel.setForeground(Color.red);
        
        JOptionPane.showMessageDialog(this, Message,"Input Error", JOptionPane.ERROR_MESSAGE);
    }
    
    public void WriteToFile(String fulName, String studentId, String email, String type) {
        BufferedWriter writer = null;
        try {
            writer = new BufferedWriter(new FileWriter("StudentReg.txt"));
            
            writer.write(studentId + "," + fulName + "," + email + "," + type);
            writer.newLine();
        }
        catch (IOException e) {
            showError("Error writing to file: "+ e.getMessage());
        }
        finally {
            try {
                writer.close();
            } catch (IOException ex) {
                System.getLogger(StudentRegJFrame.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
    }
    
